package com.wanniu.game.guild.guidDepot;

public class GuildCond {
	public int level;
	public int upLevel;
	public int job;
	public int qColor;

	public GuildCond() {}

	public GuildCond(int level, int upLevel, int job, int qColor) {
		this.level = level;
		this.upLevel = upLevel;
		this.job = job;
		this.qColor = qColor;
	}
}
