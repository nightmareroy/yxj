package com.wanniu.game.petNew;

public class PetSkill {

	public int id;
	public int level;
	public int pos;
	public int inborn;

	public PetSkill() {

	}

	public PetSkill(int id, int level, int pos, int inborn) {
		this.id = id;
		this.level = level;
		this.pos = pos;
		this.inborn = inborn;
	}

}